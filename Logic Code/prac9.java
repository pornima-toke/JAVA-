//Q10. Write a java program to count the number of digits in a number.

import java.util.*;
public class prac9
{
	public static void main(String x[])
	{
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the number");
		int n = sc.nextInt();
		int i = 1;
		while(i <= n)
		{
			System.out.println(i);
			i++;
		}
	}
}